#include <REGX52.H>        // STC89C52单片机寄存器定义头文件
#include "my_interrupt.h"  // 中断初始化函数声明

/**
 * @brief  中断及定时器初始化函数
 * @note   配置定时器0、定时器2、外部中断1及中断优先级
 */
void Interrupt_Init(void) {
    // ===== 定时器 T0 配置 =====
    TMOD &= 0xF0;      // 清除定时器0模式位
    TMOD |= 0x01;      // 设置定时器0为模式1（16位定时）
    TL0 = 0x00;        // 设置定时初值
    TH0 = 0xB8;        // 设置定时初值
    TF0 = 0;           // 清除TF0标志
    TR0 = 1;           // 启动定时器0
    ET0 = 1;           // 允许定时器0中断

    // ===== 定时器 T2 配置 =====
    T2MOD = 0;         // 初始化T2模式寄存器
    T2CON = 0;         // 初始化T2控制寄存器
    TL2 = 0x8F;        // 设置定时初值
    TH2 = 0xFE;        // 设置定时初值
    RCAP2L = 0x8F;     // 设置定时重载值
    RCAP2H = 0xFE;     // 设置定时重载值
    TR2 = 0;           // 定时器2不启动
    ET2 = 1;           // 允许定时器2中断

    // ===== 外部中断 INT1 配置（下降沿触发） =====
    IT1 = 1;           // INT1设置为下降沿触发
    EX1 = 1;           // 允许外部中断1

    // ===== 中断优先级配置 =====
    PT2 = 1;           // 定时器2设为高优先级
    PT0 = 0;           // 定时器0设为普通优先级
    PX1 = 1;           // INT1设为高优先级
    EA  = 1;           // 开启总中断
}

